while 15309 <= i ** 9 <= 10 ** 9:
    n = i ** 9

    if n % 2 == 0:
        print(i, n // 2)
    else:
        for x in range(3, int(n ** 0.5) + 1, 2):
            if n % x == 0:
                print(i, n // x)
                break

    i += 1

